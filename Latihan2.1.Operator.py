# Program menghitung kecepatan
# Rumus yang digunakan: v = s / t

# Meminta input jarak dari user
jarak = float(input("Masukkan jarak (meter): "))

# Meminta input waktu dari user
waktu = float(input("Masukkan waktu (detik): "))

# Menghitung kecepatan menggunakan operator pembagian
kecepatan = jarak / waktu

# Menampilkan hasil perhitungan
print("Kecepatan adalah:", kecepatan, "m/s")
